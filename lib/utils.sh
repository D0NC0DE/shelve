if tput setaf 1 &>/dev/null; then
  RESET="$(tput sgr0)"
  BOLD="$(tput bold)"
  RED="$(tput setaf 1)"
  GREEN="$(tput setaf 2)"
  YELLOW="$(tput setaf 3)"
  MAGENTA="$(tput setaf 5)"
  CYAN="$(tput setaf 6)"
  DIM="$(tput setaf 8)"
else
  RESET="" BOLD="" RED="" GREEN="" YELLOW="" MAGENTA="" CYAN="" DIM=""
fi

# -----------------------------------------------------------------------------
# LOGGING
# log_warn and log_error write to stderr ‚ÄĒ everything else to stdout
# -----------------------------------------------------------------------------
log_info() {
  printf "${CYAN}‚ÄĘ${RESET} %s\n" "$*"
}

log_success() {
  printf "${GREEN}‚úď${RESET} %s\n" "$*"
}

log_warn() {
  printf "${YELLOW}‚ö†${RESET} %s\n" "$*" >&2
}

log_error() {
  printf "${RED}‚úó${RESET} ${BOLD}%s${RESET}\n" "$*" >&2
}

log_step() {
  printf "\n${BOLD}${CYAN}‚Ė∂ %s${RESET}\n" "$*"
}

log_dim() {
  printf "${DIM}  %s${RESET}\n" "$*"
}

