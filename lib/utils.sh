# -----------------------------------------------------------------------------
# PROMPTS
# /dev/tty reads from the keyboard directly ‚ÄĒ safe even when stdin is piped
# -----------------------------------------------------------------------------
shelve_ask() {
  local response
  printf "${YELLOW}?${RESET} %s ${DIM}[y/N]${RESET} " "$*"
  read -r response </dev/tty
  [[ "$response" =~ ^[Yy]$ ]]
}

# -----------------------------------------------------------------------------
# COMMAND CHECKS
# -----------------------------------------------------------------------------
command_exists() {
  command -v "$1" &>/dev/null
}

app_installed() {
  [[ -d "/Applications/${1}.app" ]] ||
    [[ -d "${HOME}/Applications/${1}.app" ]]
}

# -----------------------------------------------------------------------------
# BREW CHECKS
# Check directly against Cellar/Caskroom ‚ÄĒ avoids calling brew list in a loop
# -----------------------------------------------------------------------------
brew_package_installed() {
  [[ -d "$(brew --prefix)/Cellar/${1}" ]]
}

brew_cask_installed() {
  [[ -d "$(brew --prefix)/Caskroom/${1}" ]]
}

# -----------------------------------------------------------------------------
# JSON HELPERS
# Used by save.sh and fresh.sh to build shelve.json
# -----------------------------------------------------------------------------
array_to_json() {
  local items=("$@")
  if [[ ${#items[@]} -eq 0 ]]; then
    echo "[]"
    return
  fi
  local result
  result=$(printf '"%s",' "${items[@]}")
  echo "[${result%,}]"
}

# -----------------------------------------------------------------------------
# SHELVE PATHS
# -----------------------------------------------------------------------------
export SHELVE_DIR="${HOME}/.shelve"
export SHELVE_CONFIG="${SHELVE_DIR}/shelve.json"

ensure_shelve_dir() {
  [[ -d "$SHELVE_DIR" ]] || mkdir -p "$SHELVE_DIR"
}

